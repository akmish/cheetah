package com.gsd.sreenidhi.automation.config;

public class DriverConfigurator {
	public String ieDriverPath;
	public String chromeDriverPath;
	public String firefoxDriverPath;
	public String edgeDriverPath;
	public String operaDriverPath;
	
	/**
	 * @return the ieDriverPath
	 */
	public String getIeDriverPath() {
		return ieDriverPath;
	}
	/**
	 * @param ieDriverPath the ieDriverPath to set
	 */
	public void setIeDriverPath(String ieDriverPath) {
		this.ieDriverPath = ieDriverPath;
	}
	/**
	 * @return the chromeDriverPath
	 */
	public String getChromeDriverPath() {
		return chromeDriverPath;
	}
	/**
	 * @param chromeDriverPath the chromeDriverPath to set
	 */
	public void setChromeDriverPath(String chromeDriverPath) {
		this.chromeDriverPath = chromeDriverPath;
	}
	/**
	 * @return the firefoxDriverPath
	 */
	public String getFirefoxDriverPath() {
		return firefoxDriverPath;
	}
	/**
	 * @param firefoxDriverPath the firefoxDriverPath to set
	 */
	public void setFirefoxDriverPath(String firefoxDriverPath) {
		this.firefoxDriverPath = firefoxDriverPath;
	}
	/**
	 * @return the operaDriverPath
	 */
	public String getOperaDriverPath() {
		return operaDriverPath;
	}
	/**
	 * @param operaDriverPath the operaDriverPath to set
	 */
	public void setOperaDriverPath(String operaDriverPath) {
		this.operaDriverPath = operaDriverPath;
	}
	/**
	 * @return the edgeDriverPath
	 */
	public String getEdgeDriverPath() {
		return edgeDriverPath;
	}
	/**
	 * @param edgeDriverPath the edgeDriverPath to set
	 */
	public void setEdgeDriverPath(String edgeDriverPath) {
		this.edgeDriverPath = edgeDriverPath;
	}
	

}
